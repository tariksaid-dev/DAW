    private void btnConvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConvertirActionPerformed
        if (txtEuros.getText().matches("^[0-9.]+$")) {

            double euros = Double.parseDouble(txtEuros.getText());
            double dolares = euros * 1.05;
            double libras = euros * 0.89;
            double yens = euros * 144.76;
            double pesetas = euros * 166.38;

            NumberFormat nf = NumberFormat.getInstance();

            txtDolares.setText(NumberFormat.getInstance(Locale.US).format(dolares));
            txtLibras.setText(NumberFormat.getInstance(Locale.UK).format(libras));
            txtYens.setText(NumberFormat.getInstance(Locale.JAPAN).format(yens));
            txtPesatas.setText(pesetas + " pts");
        } else {
            JOptionPane.showMessageDialog(this, "Error, introduce una cifra", "Error", NORMAL);
        }
    }//GEN-LAST:event_btnConvertirActionPerformed

